const MAIN_VERDICT = {
  red: 'По результатам аудита выявлен ряд нарушений, способных привести к штрафам, претензиям контролирующих органов и дополнительным расходам. Основная проблема заключается не в количестве нарушений, а в наличии нескольких критических зон риска, требующих первоочередного внимания. Рекомендуем начать устранение с нарушений, имеющих риск 9–10 баллов.',
  yellow: 'Ваш бизнес находится в относительно стабильном состоянии, однако выявлены зоны, которые рекомендуется привести в порядок до возникновения проверок или конфликтных ситуаций. Большинство нарушений могут быть устранены без существенных финансовых затрат.',
  green: 'Система не выявила критических нарушений. Это хороший результат. Тем не менее рекомендуем периодически пересматривать документацию и следить за изменениями законодательства.',
};

const READINESS = {
  red: { label: 'Низкая', text: 'Существует высокая вероятность выявления нарушений.' },
  yellow: { label: 'Средняя', text: 'Большинство критических рисков контролируются, однако остаются зоны внимания.' },
  green: { label: 'Высокая', text: 'Критические нарушения не выявлены.' },
};

const RISK_MANAGEMENT_TEXT =
  'Для большинства предпринимателей основная проблема заключается не в отсутствии документов, а в отсутствии единой системы контроля. Рекомендуем: хранить документы централизованно; назначить ответственного за обновление документов; проводить внутреннюю проверку не реже одного раза в квартал; отслеживать изменения законодательства; регулярно пересматривать договоры и согласия.';

// violations уже отсортированы по риску (scoring.sortByRisk) при вызове.
function buildRecommendations({ violations, zone, forecast }) {
  const topActions = violations.slice(0, 3);
  const freeFixes = violations.filter((v) => v.free);
  const needsSpecialist = violations.filter((v) => !v.free || (v.costMin || 0) > 5000);

  return {
    mainVerdict: MAIN_VERDICT[zone],
    topActions,
    freeFixes,
    needsSpecialist,
    forecast,
    riskManagementText: RISK_MANAGEMENT_TEXT,
    readiness: READINESS[zone],
  };
}

module.exports = { buildRecommendations };
