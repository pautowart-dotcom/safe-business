-- Оферта/политика конфиденциальности (0013) были черновиком с плейсхолдерами
-- ([домен], [срок]) — с тех пор появился реальный домен, тариф и политика
-- хранения данных (см. FAQ, migrations/0039). Точечные замены текста, а не
-- перезапись всего документа — так видно ровно, что изменилось. То, что
-- по-прежнему не решено (ФИО ИП/ОГРНИП/ИНН, вопрос юриста про поштучную
-- оплату филиалов, рабочий email) — НЕ трогаем, это реальные факты/решения
-- владельца или юриста, не мои.
--
-- WHERE content LIKE — не переписывает документ, если владелец (или кто-то
-- ещё) уже отредактировал его вручную через админку и старого текста-
-- заготовки там больше нет: тогда REPLACE просто не найдёт, что менять,
-- и UPDATE безопасно затронет 0 строк вместо порчи чужой правки.

UPDATE legal_documents
SET content = REPLACE(
  content,
  'сайт/приложение по адресу [домен]',
  'сайт business-safe.ru (приложение — app.business-safe.ru)'
),
updated_at = now()
WHERE key = 'oferta' AND content LIKE '%сайт/приложение по адресу [домен]%';

